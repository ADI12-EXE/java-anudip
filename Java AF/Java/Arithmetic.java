public class Arithmetic 
{
    public static void main(String[] args) 
    {
        int num1 = 7;
        int num2 = 5;

        int result = num1 + num2;
        System.out.println(result);
        int sub = num1 - num2;
        System.out.println(sub);
        int div = num1 / num2;
        System.out.println(div);
        int multi = num1 * num2;
        System.out.println(multi);



    }
}
